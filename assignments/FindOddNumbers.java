package assignments;

public class FindOddNumbers {

	public static void main(String[] args) {
	System.out.println("The odd numbers between 1 to 10 ");
	int maxRange = 10;
	
	for (int i = 1; i <= maxRange; i++) {

		if (i % 2 == 1) {
			
			System.out.println(i); 
		}

		
	}

	
	
	
	
}
}
